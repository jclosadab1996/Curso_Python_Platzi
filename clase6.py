# Operadores numericos

a = 10
b = 3

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)

print("Modulo", a % b) # Módulo
print("Parte entera division", a // b) # Parte entera de la división
print("Potencia:", a ** b) # Potencia

a = a + 2
print("Suma:", a) # Suma de dos números, el valor obtenido es 12

operation = 2 + 3 * 4
print("Operación:", operation) # Operación con precedencia, el resultado es 14

operation_2= 2 + (3 * 4)
print("Operación 2:", operation_2) # Operación con precedencia, el resultado es 14

